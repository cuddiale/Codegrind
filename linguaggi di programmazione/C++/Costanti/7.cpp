// Definire una costante boolean e utilizzarla in un'operazione logica.

#include <iostream>
using namespace std;

int main() {
    const bool VERITA = true;

    if (VERITA) {
        cout << "La costante è vera." << endl;
    } else {
        cout << "La costante è falsa." << endl;
    }

    return 0;
}
// Scrivi un programma Java che verifica se un numero intero è positivo, negativo o zero.

int numero = 5;
if (numero > 0) {
    System.out.println("Il numero è positivo.");
} else if (numero < 0) {
    System.out.println("Il numero è negativo.");
} else {
    System.out.println("Il numero è zero.");
}